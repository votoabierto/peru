// scripts/generate-data-freshness.mjs
// Run during build (prebuild script) to generate lib/generated/data-freshness-meta.ts

import { execSync } from 'child_process'
import { writeFileSync, mkdirSync } from 'fs'

const files = {
  presidential: 'data/candidates.json',
  senate: 'data/senate-candidates.json',
  diputados: 'data/diputados-candidates.json',
  andino: 'data/andino-candidates.json',
  parties: 'data/parties.json',
}

const meta = {}
for (const [key, file] of Object.entries(files)) {
  try {
    const date = execSync(`git log -1 --format="%ci" -- ${file}`, { encoding: 'utf8' }).trim()
    meta[key] = date || null
  } catch {
    meta[key] = null
  }
}

mkdirSync('lib/generated', { recursive: true })
writeFileSync(
  'lib/generated/data-freshness-meta.ts',
  `// Auto-generated by scripts/generate-data-freshness.mjs — do not edit\nexport const DATA_FRESHNESS = ${JSON.stringify(meta, null, 2)} as const\n`
)

console.log('Generated lib/generated/data-freshness-meta.ts')
